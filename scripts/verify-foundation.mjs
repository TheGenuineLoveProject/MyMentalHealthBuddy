#!/usr/bin/env node
/**
 * Governed verification foundation — structured, non-destructive.
 *
 * PREVENTION-FIRST CONTRACT:
 *  - This script NEVER runs `vite build`. A fresh build empties client/dist for
 *    ~50s, which 500s every `/` request and can fail the health check on the
 *    live server. Build is verified separately/manually. Here we only CHECK
 *    that the build artifact already exists.
 *  - SCOPE: artifact PRESENCE only. Build freshness/content (whether dist
 *    matches current source) is intentionally NOT verified here.
 *  - Hard gates (cause non-zero exit): build_artifact present, api_health == 200.
 *  - Soft checks (informational only): readyz, healthz.
 *  - Emits one structured JSONL record to logs/verification.jsonl (same
 *    convention as logs/events.jsonl, logs/uptime.jsonl).
 *  - Regenerates docs/architecture/platform-status.md as an auto-generated
 *    snapshot (the "architecture memory" surface).
 */

import { appendFileSync, existsSync, mkdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const ROOT = process.cwd();
const LOG_FILE = join(ROOT, "logs", "verification.jsonl");
const STATUS_DOC = join(ROOT, "docs", "architecture", "platform-status.md");
const BASE = process.env.VERIFY_BASE_URL || "http://localhost:5000";

const HARD = new Set(["build_artifact", "api_health"]);
const checks = [];
const record = (name, ok, detail) => checks.push({ name, ok, detail });

async function httpStatus(path) {
  const url = `${BASE}${path}`;
  try {
    const res = await fetch(url, { signal: AbortSignal.timeout(5000) });
    return { url, status: res.status };
  } catch (err) {
    return { url, status: 0, error: err?.name || String(err) };
  }
}

// 1. Build artifact (hard gate) — proves a usable client/dist is present.
const indexHtml = join(ROOT, "client", "dist", "index.html");
if (existsSync(indexHtml)) {
  record("build_artifact", true, `present (${statSync(indexHtml).size} bytes)`);
} else {
  record("build_artifact", false, "MISSING client/dist/index.html — run `npm run build`");
}

// 2. API health (hard gate).
const health = await httpStatus("/api/health");
record("api_health", health.status === 200, `${health.url} -> ${health.status}${health.error ? ` (${health.error})` : ""}`);

// 3. Soft/informational runtime probes.
const healthz = await httpStatus("/healthz");
record("healthz", healthz.status === 200, `${healthz.url} -> ${healthz.status}`);
const ready = await httpStatus("/readyz");
record("readyz", ready.status === 200, `${ready.url} -> ${ready.status}`);

const hardChecks = checks.filter((c) => HARD.has(c.name));
const ok = hardChecks.every((c) => c.ok);
const ts = new Date().toISOString();
const result = { ts, event: "verification", ok, base: BASE, checks };

// Structured JSONL log (append-only, matches existing logs/*.jsonl convention).
mkdirSync(dirname(LOG_FILE), { recursive: true });
appendFileSync(LOG_FILE, JSON.stringify(result) + "\n");

// Auto-generated architecture-memory snapshot.
const rows = checks
  .map((c) => `| ${c.name} | ${HARD.has(c.name) ? "hard" : "soft"} | ${c.ok ? "PASS" : "FAIL"} | ${c.detail} |`)
  .join("\n");
const doc = `# Platform Status (auto-generated)

> Generated by \`scripts/verify-foundation.mjs\`. Do NOT edit by hand — it is
> overwritten on every \`npm run verify\` / \`npm run verify:foundation\`.
> Structured history: \`logs/verification.jsonl\`.

- **Last verified:** ${ts}
- **Base URL:** ${BASE}
- **Overall (hard gates):** ${ok ? "PASS" : "FAIL"}

| check | gate | result | detail |
| --- | --- | --- | --- |
${rows}

Hard gates must pass for the foundation to be considered green. Soft checks are
informational (they do not fail the run).
`;
mkdirSync(dirname(STATUS_DOC), { recursive: true });
writeFileSync(STATUS_DOC, doc);

// Console summary.
console.log(`[verify-foundation] ${ok ? "PASS" : "FAIL"} @ ${ts}`);
for (const c of checks) {
  console.log(`  [${c.ok ? "ok" : "XX"}] (${HARD.has(c.name) ? "hard" : "soft"}) ${c.name}: ${c.detail}`);
}
console.log(`[verify-foundation] log -> ${LOG_FILE}`);
console.log(`[verify-foundation] status -> ${STATUS_DOC}`);

process.exit(ok ? 0 : 1);
